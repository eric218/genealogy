package com.dct.swocean.util.dataimport;

import java.util.List;

public class ExcelResult {

	private List<String>  headerContent;
	private List<String[]>	dataContent;
	
	public List<String> getHeaderContent() {
		return headerContent;
	}
	public void setHeaderContent(List<String> headerContent) {
		this.headerContent = headerContent;
	}
	public List<String[]> getDataContent() {
		return dataContent;
	}
	public void setDataContent(List<String[]> dataContent) {
		this.dataContent = dataContent;
	}
	@Override
	public String toString() {
		return "ExcelResult [headerContent=" + headerContent + ", dataContent=" + dataContent + "]";
	}
	
}
